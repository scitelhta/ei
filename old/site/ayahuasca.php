<h2 style="margin:10px;">Ayahuasca</h2>

<p>El t&eacute; conocido como <i>Ayahuasca</i>, <i>Yag&eacute;</i> o <i>Daime</i> es una cocci&oacute;n conjunta de dos plantas: 
Una liana llamada por la ciencia <i>Banisteriopsis caapi</i> y una planta que com&uacute;nmente es la <i>Chacruna (Psychotria viridis)</i> 
o la <i>Chacropanga (Diplopterys cabrerana)</i>. </p>
<p>El matrimonio de estos dos esp&iacute;ritus vegetales se conectan formando una medicina que incrementa la sensibilidad de nuestro sistema
y nos gu&iacute;a en el aprendizaje de la recepci&oacute;n, procesamiento y respuesta de los est&iacute;mulos de nuestros sentidos para
que llevemos a cabo la armonizaci&oacute;n de nuestra energ&iacute;a y de nuestros pensamientos y desarrollemos la voluntad.</p>

<p>Los procesos de sanaci&oacute;n f&iacute;sico-psicol&oacute;gicos que provocan la ingesta de la medicina se suelen llevar con mucha responsabilidad
en rituales tradicionales bajo la orientaci&oacute;n de un ente familiarizado en ellos para apoyar a que sea equilibrada su asimilaci&oacute;n.</p>

<h4 style="margin:10px;">Or&iacute;gen Cultural</h4>

<p>La elaboraci&oacute;n de la  <i>Ayahuasca</i> fue descubierta por culturas ind&iacute;genas ancestrales de las selvas de Suram&eacute;rica
en donde las plantas crecen de forma silvestre. Por varios miles de a&ntilde;os nos ha acompa&ntilde;ado la medicina impulsando el perfeccionamiento
de incontables individuos y de varias cadenas de sociedades que han transmitido y madurado tradiciones basadas en las caracer&iacute;sticas
&uacute;nicas de cada cual.</p>

<h4 style="margin:10px;">Yag&eacute; Urbano</h4>

<p>En los tiempos modernos la toma de la medicina ha tenido un papel importante en el conocimiento del hombre sobre s&iacute; mismo.</p>
<p>La transculturizaci&oacute;n permite que costumbres anteriormente exclusivas de algunas civilizaciones, crezcan y se adapten a las que
se est&aacute;n formando en un mundo de r&aacute;pidos movimientos. La <i>Ayahuasca</i> ha llegado a nuestras ciudades y hasta los m&aacute;s
dificultosos barrios, no s&oacute;lo de la mano de entes ind&iacute;genas sino tambi&eacute;n de aquellos que se han levantado en sus propios pies
y han asumido el manejo de esta sagrada medicina.</p>
<p>Nosotros promovemos y organizamos el uso responsable del <i>Yag&eacute;</i> para el desarrollo personal nuestro y de nuestros hermanos
con el resultado del mejoramiento de las sociedades y el incremento del bienestar de nuestro planeta.</p>





